# Lista
nomes = ["Fulano", "Cicrano", "Beltrano", "João", "Maria", "José"]
nomes.sort() # imprimir na tela em ordem alfabetica
nomes.sort(reverse=True) # imprime a lista em ordem alfabetica infersa (z-a)

#imprimir a lista na tela
print(nomes)

# imprimir na sequencia desejada
print(nomes[0])
print(nomes[3])

# imprimir com laço for
for nome in nomes:
    print(nome)


